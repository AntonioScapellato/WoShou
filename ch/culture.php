                          <p style="color:#000; text-align: justify;">中秋节是民间的传统节日，节期在农历的八月十五日。它有许多别称：月夕、祭月节、月亮节，团圆节等，皆因中秋夜月亮圆满，对中国人来说象征团圆。<br>
中秋节历史悠久，从唐代开始古人已有赏月与祭拜月神的习俗。如今虽然庆祝方式会随着地区有些许变化，但不变的是人们都会与家人团聚，一起赏月、吃月饼、放花灯。
</p>
