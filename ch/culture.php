                          <h2 class="h4  mb-3"><a>农历</a></h2>
                          <p style="color:#000; text-align: justify;">农历是中国传统历法，它是一种以阴历（月亮围绕地球转动的规律）为基础，融合阳历元素而形成的阴阳合历。
                            从数千年前的干支纪元起，它的基础规则历经多代逐渐完善。现今农历仍广泛应用于中国乃至东亚地区的传统节庆中。<br>
农历中的年份有两种：12个月的平年与13个月的闰年。月份则分为30天的大月与29天的小月。根据置闰法，每19年中会设置7个闰年来平衡平均历年与太阳回归年间的天数差。每个月的初一，月球以黑暗面朝向地球。
但到了十五、十六，则是被太阳照亮的那一半正对地球，此时看到的便是满月。
</p>
