                          <h2 class="h4  mb-3"><a>新年</a></h2>
                          <p style="color:#000; text-align: justify;">新年，又称春节，是中华文化中最隆重的传统节日。在现代，新年从农历正月初一开始，到正月十五的元宵节结束。
                            大年三十的时候，所有人都会放下手头的事情，与家人团聚一起吃年夜饭。<br>
新年期间的习俗各式各样，比较普遍的是走亲访友、吃饺子、欣赏焰火与舞狮表演。红色最能代表新年了，它是吉祥、喜庆的象征。人们通常会穿红色的衣服，在家里挂上红色的装扮，例如在门上倒着贴写有福字的红纸，有福到家了的意思。
