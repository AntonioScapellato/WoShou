                          <h2 class="h4  mb-3"><a>Chinese New Year</a></h2>
                          <p style="color:#000; text-align: justify;">Chinese New Year, known as the Spring Festival, generally falls between January and February. It is celebrated for two consecutive weeks and it ends with the Lantern Festival (the 15th day of the 1st month according to the Chinese Calendar). It represents the most important festival in the traditional Chinese culture. Everyone sets aside personal matters and meet with the family on New Year’s Eve.<br>
During this period it is tradition visiting relatives and close friends, eating dumplings, watching fireworks and lion dances. Red colour, symbol of enthusiasm and happiness, is the best choice for welcoming the New Year. Indeed, people commonly wear red clothes and decorate the house with red things, for example red papers with 福 (FU) written are put backwards on the door to attract good luck.
